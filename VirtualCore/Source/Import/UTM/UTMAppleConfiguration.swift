import Foundation
import BuddyFoundation

extension PropertyListDecoder {
    static let utm = PropertyListDecoder()
}

extension UTMAppleConfiguration {
    init(path: FilePath) throws {
        let data: Data = try path.read()
        let stub = try PropertyListDecoder.utm.decode(UTMConfigurationStub.self, from: data)

        guard stub.backend == "Apple" else {
            throw "Only UTM virtual machines based on Apple Virtualization are supported."
        }

        self = try PropertyListDecoder.utm.decode(UTMAppleConfiguration.self, from: data)
    }
}

