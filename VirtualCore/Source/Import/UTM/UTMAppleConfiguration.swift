struct UTMAppleConfigurationVirtualization: Decodable {
    enum PointerDevice: String, Codable {
        case disabled = "Disabled"
        case mouse    = "Mouse"
        case trackpad = "Trackpad"
    }

    enum KeyboardDevice: String, Codable {
        case disabled = "Disabled"
        case generic  = "Generic"
        case mac      = "Mac"
    }

    var hasAudio: Bool
    var hasBalloon: Bool
    var hasEntropy: Bool
    var keyboard: KeyboardDevice
    var pointer: PointerDevice
    var hasRosetta: Bool?
    var hasClipboardSharing: Bool

    private enum CodingKeys: String, CodingKey {
        case hasAudio            = "Audio"
        case hasBalloon          = "Balloon"
        case hasEntropy          = "Entropy"
        case keyboard            = "Keyboard"
        case pointer             = "Pointer"
        case hasTrackpad         = "Trackpad"
        case rosetta             = "Rosetta"
        case hasClipboardSharing = "ClipboardSharing"
    }

    init(from decoder: Decoder) throws {
        let c = try decoder.container(keyedBy: CodingKeys.self)
        hasAudio            = try c.decode(Bool.self, forKey: .hasAudio)
        hasBalloon          = try c.decode(Bool.self, forKey: .hasBalloon)
        hasEntropy          = try c.decode(Bool.self, forKey: .hasEntropy)

        if let kbBool = try? c.decode(Bool.self, forKey: .keyboard) {
            keyboard = kbBool ? .generic : .disabled
        } else {
            keyboard = try c.decode(KeyboardDevice.self, forKey: .keyboard)
        }

        if let ptBool = try? c.decode(Bool.self, forKey: .pointer) {
            let tp = try c.decodeIfPresent(Bool.self, forKey: .hasTrackpad) ?? false
            pointer = tp ? .trackpad : (ptBool ? .mouse : .disabled)
        } else {
            pointer = try c.decode(PointerDevice.self, forKey: .pointer)
        }

        hasRosetta           = try c.decodeIfPresent(Bool.self, forKey: .rosetta)
        hasClipboardSharing  = try c.decode(Bool.self, forKey: .hasClipboardSharing)
    }
}
